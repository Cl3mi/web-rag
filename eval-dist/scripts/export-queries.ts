/**
 * Dump the operator's current test_queries (+ referenced documents) to a SQL
 * seed file that the eval container's Postgres loads on first boot.
 *
 * Run from project root *before* building the image:
 *   bun run eval-dist/scripts/export-queries.ts
 *
 * The eval container ships the same questions + ground-truth URLs the
 * operator uses internally, so both systems are evaluated against an
 * identical query set.
 */

import { sql } from '../../src/lib/server/db/client';
import { writeFileSync } from 'fs';
import { join } from 'path';

const OUT = join(import.meta.dir, '..', 'seed', 'queries.sql');

function esc(v: unknown): string {
  if (v === null || v === undefined) return 'NULL';
  if (typeof v === 'number') return String(v);
  if (typeof v === 'boolean') return v ? 'TRUE' : 'FALSE';
  if (Array.isArray(v) || typeof v === 'object') {
    return `'${JSON.stringify(v).replace(/'/g, "''")}'::jsonb`;
  }
  return `'${String(v).replace(/'/g, "''")}'`;
}

const queries = await sql<Array<{
  id: string; query: string; expected_document_ids: string[];
  category: string | null; difficulty: string | null;
}>>`SELECT id, query, expected_document_ids, category, difficulty FROM test_queries`;

const docIds = [...new Set(queries.flatMap(q => q.expected_document_ids ?? []))];
const documents = docIds.length
  ? await sql<Array<{ id: string; url: string; title: string | null }>>`
      SELECT id, url, title FROM documents WHERE id = ANY(${docIds}::uuid[])`
  : [];

const lines: string[] = [
  '-- Auto-generated by eval-dist/scripts/export-queries.ts',
  '-- DO NOT EDIT BY HAND.',
  '',
  'BEGIN;',
  '',
  '-- Minimal documents table — only fields the queries endpoint needs.',
  'CREATE TABLE IF NOT EXISTS documents (',
  '  id uuid PRIMARY KEY,',
  '  url text NOT NULL,',
  '  title text',
  ');',
  '',
  'CREATE TABLE IF NOT EXISTS test_queries (',
  '  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),',
  '  query text NOT NULL,',
  '  expected_document_ids jsonb NOT NULL,',
  '  category varchar(100),',
  '  difficulty varchar(20),',
  '  metadata jsonb DEFAULT \'{}\'::jsonb,',
  '  created_at timestamptz NOT NULL DEFAULT now()',
  ');',
  '',
  'TRUNCATE documents, test_queries;',
  '',
];

for (const d of documents) {
  lines.push(`INSERT INTO documents (id, url, title) VALUES (${esc(d.id)}, ${esc(d.url)}, ${esc(d.title)});`);
}
lines.push('');
for (const q of queries) {
  lines.push(
    `INSERT INTO test_queries (id, query, expected_document_ids, category, difficulty) VALUES (` +
    `${esc(q.id)}, ${esc(q.query)}, ${esc(q.expected_document_ids)}, ${esc(q.category)}, ${esc(q.difficulty)});`
  );
}
lines.push('', 'COMMIT;', '');

writeFileSync(OUT, lines.join('\n'));
console.log(`Wrote ${queries.length} queries + ${documents.length} documents → ${OUT}`);
process.exit(0);
